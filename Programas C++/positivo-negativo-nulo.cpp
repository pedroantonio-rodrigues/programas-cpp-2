#include<iostream>

using namespace std;

int main (){
	
	int numero = 0;
	 cout << "digite um numero: ";
	 cin >> numero;
	
	if (numero = 0 ){
		cout << "numero nulo";	
	} else if (numero < 0 ){
		cout << "numero negativo";
	} if (numero > 0){
		cout << "numero positivo";
	}
	
	return 0;
}
